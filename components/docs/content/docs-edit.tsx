export function DocsEdit() {
  return (
    <article className="prose prose-lg prose-docs dark:prose-invert">
      <p>
        作成した予定は右クリックで編集および削除が可能です。スマートフォンからアクセスしている方は、予定をタップすることで同様の操作を行えます。
      </p>

      <h2>予定の編集</h2>
      <p>
        予定を右クリック（モバイルではタップ）すると、編集ダイアログが表示されます。基本的に新規作成時と使い勝手は同じです。タイトル、日時、通知設定、色などを変更できます。
      </p>

      <h2>予定の削除</h2>
      <p>
        編集ダイアログの下部に予定削除のボタンがあります。削除ボタンをクリックすると、予定が完全に削除されます。
      </p>

      <h2>Discordコマンドから編集・削除する</h2>
      <p>
        ブラウザを開かずに、Discord上から直接編集・削除を行うこともできます。
      </p>
      <ul>
        <li>
          <code>/edit event:&lt;予定名&gt;</code>
          で編集モーダルを表示、または個別の項目を引数で上書きできます
        </li>
        <li>
          <code>/delete event:&lt;予定名&gt;</code>
          で確認ダイアログを表示し、削除を実行できます
        </li>
      </ul>
      <p>
        予定名は部分一致で検索されます。詳しい使い方は
        <a href="/docs/commands">コマンド一覧</a>
        をご確認ください。
      </p>

      <h2>注意事項</h2>
      <p>
        サーバー側の設定で予定の作成や変更が制限されている場合、編集・削除の操作は行えません。サーバーの管理者にお問い合わせください。
      </p>
    </article>
  );
}
