export function DocsCommands() {
  return (
    <article className="prose prose-lg prose-docs dark:prose-invert">
      <p>Discalendarで使用できるDiscordスラッシュコマンドの一覧です。</p>

      <h2>/init</h2>
      <p>
        Botの通知の送信先を設定します。引数を省略した場合は、コマンドを実行したチャンネルが通知先になります。
      </p>
      <p>
        <code>channel</code>
        オプションで別のテキストチャンネルを指定することも可能です。
      </p>
      <p>
        このコマンドは「メッセージの管理」「サーバーの管理」「ロールの管理」「管理者」のいずれかの権限を持っている方、もしくはサーバーのオーナーが実行できます。
      </p>

      <h2>/list</h2>
      <p>
        サーバーに登録されている予定のリストを表示します。引数を省略すると未来の予定のみが表示されます。
      </p>
      <p>
        <code>range</code> オプションで <code>過去</code> /{" "}
        <code>未来</code> / <code>全て</code>{" "}
        を選択することで、表示範囲を切り替えられます。繰り返し予定は最大90日先までのオカレンスが展開されて表示されます。
      </p>
      <p>
        4件を超える場合はページ送り（◀ / ▶ ボタン）で切り替えられます。
      </p>

      <h2>/create</h2>
      <p>
        Discord上から直接、予定を作成できます。引数の説明はDiscordが自動で補完してくれます。引数を省略した場合は入力モーダルが表示されます。
      </p>
      <p>
        日時は日本標準時（JST）で指定してください。配色や事前通知（最大4件）も同時に指定できます。
      </p>
      <p>
        サーバー側の設定で予定の作成が制限されている場合は、上述の管理権限が必要になります。
      </p>

      <h2>/edit</h2>
      <p>
        既存の予定を編集します。<code>event</code>{" "}
        オプションで対象の予定名（部分一致）を指定し、変更したい項目だけを引数で渡します。
      </p>
      <p>
        変更項目を1つも指定しなかった場合は、現在値が入った編集モーダルが表示されます。<code>description</code>{" "}
        に空文字を渡すと説明を削除できます。
      </p>
      <p>
        サーバー側の設定で予定の編集が制限されている場合は、上述の管理権限が必要になります。
      </p>

      <h2>/delete</h2>
      <p>
        既存の予定を削除します。<code>event</code>{" "}
        オプションで対象の予定名（部分一致）を指定すると、対象予定の内容と確認ダイアログが表示されます。
      </p>
      <p>
        「削除する」ボタンを押すと予定が完全に削除されます。誤操作を避けるため、確認は60秒でタイムアウトし、コマンド実行者本人のみが操作できます。
      </p>
      <p>
        サーバー側の設定で予定の削除が制限されている場合は、上述の管理権限が必要になります。
      </p>

      <h2>/rsvp</h2>
      <p>
        予定への出欠を回答します。<code>event</code>{" "}
        オプションで対象の予定名（部分一致）を指定し、<code>status</code>{" "}
        オプションで <code>参加</code> / <code>未定</code> /{" "}
        <code>不参加</code> から選択します。
      </p>
      <p>
        既に同じステータスで回答している予定に再度同じステータスで回答すると、出欠の登録が取り消されます。
      </p>

      <h2>/help</h2>
      <p>
        ヘルプメッセージを表示します。コマンドの使い方がわからない場合はこちらを参照してください。
      </p>

      <h2>/invite</h2>
      <p>
        Botを自分のサーバーに参加させるためのリンクを表示します。他のサーバーにもBotを導入したい場合に使用してください。
      </p>
    </article>
  );
}
